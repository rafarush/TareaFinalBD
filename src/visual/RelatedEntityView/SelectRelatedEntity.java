    private void selectButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        int select = customTable.getSelectedRow();
        System.out.println("Fila: "+ select);
        if(select!=-1){
            String entity= (String) customTable.getValueAt(select,0);
            RelatedEntity re = null;
            try {
                re = ServicesLocator.getInstance().getRelatedEntityServices().obtainRelatedEntity(entity);
                if (re != null) {
                    try {
                        String pdfPath = PDFReportGenerator.createRelatedEntityReportPDF(re.getEntityName());

                        File file = new File(pdfPath);
                        if (file.exists()) {
                            Desktop.getDesktop().open(file);
                        } else {
                            throw new UnsupportedOperationException("El archivo no existe.");
                        }
                    } catch (IOException e) {

                        JOptionPane.showMessageDialog(null, "Hubo un problema al crear el reporte.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "La entidad no existe.");
                }
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una Entidad");
        }

    }

